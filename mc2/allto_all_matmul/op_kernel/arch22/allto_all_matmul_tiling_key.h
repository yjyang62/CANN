#ifndef __OP_KERNEL_ALLTO_ALL_MATMUL_TILING_KEY_H__
#define __OP_KERNEL_ALLTO_ALL_MATMUL_TILING_KEY_H__

#include "ascendc/host_api/tiling/template_argument.h"
#include "./allto_all_matmul_tiling.h"
#include "./allto_all_matmul_tiling_data_910_93.h"

#define TILINGKEY_TPL_FP16 0
#define TILINGKEY_TPL_BF16 1
#define TILINGKEY_TPL_FP32 2
#define TILINGKEY_TPL_NOQUANT 0
#define TILINGKEY_TPL_A16W8 1
#define TILINGKEY_TPL_A4W4 2
#define TILINGKEY_TPL_A16W4 3

// 对A2A3版本用模板参数区分
#define SOC_ASCEND910B 0
#define SOC_ASCEND910_93 1

// 模板参数
ASCENDC_TPL_ARGS_DECL(AlltoAllMatmul, // 算子OpType
                      ASCENDC_TPL_BOOL_DECL(ALLTO_ALL_MM_HAS_BIAS, 0, 1),
                      ASCENDC_TPL_BOOL_DECL(ALLTO_ALL_MM_TRANSPOSE_X2, 0, 1),
                      ASCENDC_TPL_UINT_DECL(ALLTO_ALL_MM_QUANT_TYPE, ASCENDC_TPL_2_BW, ASCENDC_TPL_UI_LIST,
                                            TILINGKEY_TPL_NOQUANT, TILINGKEY_TPL_A16W8, TILINGKEY_TPL_A4W4,
                                            TILINGKEY_TPL_A16W4),
                      ASCENDC_TPL_UINT_DECL(ALLTO_ALL_MM_QUANT_BIAS_DTYPE, ASCENDC_TPL_2_BW, ASCENDC_TPL_UI_LIST,
                                            TILINGKEY_TPL_FP16, TILINGKEY_TPL_BF16, TILINGKEY_TPL_FP32),
                      ASCENDC_TPL_UINT_DECL(ALLTO_ALL_MM_SOC_VERSION, ASCENDC_TPL_2_BW, ASCENDC_TPL_UI_LIST,
                                            SOC_ASCEND910B, SOC_ASCEND910_93));

// 模板参数组合
// 用于调用GET_TPL_TILING_KEY获取TilingKey时，接口内部校验TilingKey是否合法
ASCENDC_TPL_SEL(
    // A2的模板参数校验
    ASCENDC_TPL_ARGS_SEL(
        // 无quant，不需要指定bias类别
        ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_HAS_BIAS, 0, 1), ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_TRANSPOSE_X2, 0, 1),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_TYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_NOQUANT),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_BIAS_DTYPE, ASCENDC_TPL_UI_LIST, 0),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_SOC_VERSION, ASCENDC_TPL_UI_LIST, SOC_ASCEND910B),
        ASCENDC_TPL_TILING_STRUCT_SEL(AlltoAllMatmulTilingData)),
    ASCENDC_TPL_ARGS_SEL(
        // 动态量化，需要指定bias类别
        ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_HAS_BIAS, 0, 1), ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_TRANSPOSE_X2, 0, 1),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_TYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_A16W8, TILINGKEY_TPL_A16W4),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_BIAS_DTYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_FP16, TILINGKEY_TPL_BF16,
                             TILINGKEY_TPL_FP32),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_SOC_VERSION, ASCENDC_TPL_UI_LIST, SOC_ASCEND910B),
        ASCENDC_TPL_TILING_STRUCT_SEL(AlltoAllMatmulTilingData)),
    ASCENDC_TPL_ARGS_SEL(
        // 静态量化，需要指定bias类别
        ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_HAS_BIAS, 0, 1), ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_TRANSPOSE_X2, 0, 1),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_TYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_A4W4),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_BIAS_DTYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_FP16, TILINGKEY_TPL_BF16,
                             TILINGKEY_TPL_FP32),
        ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_SOC_VERSION, ASCENDC_TPL_UI_LIST, SOC_ASCEND910B),
        ASCENDC_TPL_TILING_STRUCT_SEL(AlltoAllMatmulTilingData)),
    // A3的模板参数校验
    ASCENDC_TPL_ARGS_SEL(ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_HAS_BIAS, 0, 1),
                         ASCENDC_TPL_BOOL_SEL(ALLTO_ALL_MM_TRANSPOSE_X2, 0, 1),
                         ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_TYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_NOQUANT),
                         ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_QUANT_BIAS_DTYPE, ASCENDC_TPL_UI_LIST, TILINGKEY_TPL_FP16,
                                              TILINGKEY_TPL_FP32),
                         ASCENDC_TPL_UINT_SEL(ALLTO_ALL_MM_SOC_VERSION, ASCENDC_TPL_UI_LIST, SOC_ASCEND910_93),
                         ASCENDC_TPL_TILING_STRUCT_SEL(AlltoAllMatmulTilingDataA3)));
#endif // __OP_KERNEL_ALLTO_ALL_MATMUL_TILING_KEY_H__