inline int ClampValue(int32_t value, int32_t min, int32_t max)
{
    return std::max(min, std::min(value, max));
}

inline int32_t CeilDev(int32_t num, int32_t div)
{
    if (div == 0) {
        return 0;
    }
    return (num + div - 1) / div;
}

