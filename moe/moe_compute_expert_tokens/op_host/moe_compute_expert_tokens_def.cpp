/*!
 * \file moe_compute_expert_tokens_def.cpp
 * \brief
 */
#include "register/op_def_registry.h"

namespace ops {
class MoeComputeExpertTokens : public OpDef
{
public:
    explicit MoeComputeExpertTokens(const char* name) : OpDef(name)
    {
        this->Input("sorted_experts")
            .ParamType(REQUIRED)
            .DataType({ge::DT_INT32})
            .Format({ge::FORMAT_ND})
            .UnknownShapeFormat({ge::FORMAT_ND})
            .AutoContiguous();
        this->Output("total_rows_before_expert")
            .ParamType(REQUIRED)
            .DataType({ge::DT_INT32})
            .Format({ge::FORMAT_ND})
            .UnknownShapeFormat({ge::FORMAT_ND});
        this->Attr("num_experts").Int();
        OpAICoreConfig aicore_config;
        aicore_config.DynamicCompileStaticFlag(true)
            .DynamicFormatFlag(true)
            .DynamicRankSupportFlag(true)
            .DynamicShapeSupportFlag(true)
            .NeedCheckSupportFlag(false)
            .PrecisionReduceFlag(true)
            .ExtendCfgInfo("aclnnSupport.value", "support_aclnn");

        this->AICore().AddConfig("ascend910b", aicore_config);
        this->AICore().AddConfig("ascend910_93", aicore_config);
        this->AICore().AddConfig("ascend950", aicore_config);
        this->AICore().AddConfig("kirinx90", aicore_config);
        this->AICore().AddConfig("kirin9030", aicore_config);
    }
};

OP_ADD(MoeComputeExpertTokens);
} // namespace ops
